def build_synonym_map() -> Dict[str, Set[str]]:
    m: Dict[str, Set[str]] = {}
    for cluster in GLOSS_CLUSTERS:
        for w in cluster:
            m[w] = set(cluster)
    return m


_SYN_MAP: Dict[str, Set[str]] | None = None


def synonyms_of(token: str) -> Set[str]:
    global _SYN_MAP
    if _SYN_MAP is None:
        _SYN_MAP = build_synonym_map()
    t = (token or "").lower().strip().replace("_", " ").split()[0] if token else ""
    if not t:
        return set()
    return _SYN_MAP.get(t, {t})
