def seed_records() -> List[dict]:
    import re

    out = []
    seen = set()
    for lang, form, gloss in CORE_SEEDS:
        key = f"{lang}|{form}"
        if key in seen:
            continue
        seen.add(key)
        mk = re.sub(r"[^a-z0-9]+", "_", gloss.lower()).strip("_")
        out.append(
            {
                "source_lang": lang,
                "source_word": form,
                "target_lang": "en",
                "target_word": gloss,
                "meaning_key": mk,
                "confidence": 0.99,
                "tier": "A",
                "source_title": "core_lemma_seeds",
                "is_name": False,
            }
        )
    return out


def seed_keys() -> Set[str]:
    return {f"{lang}|{form}" for lang, form, _ in CORE_SEEDS}


